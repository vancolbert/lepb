def gen_nodedims(p):
    import bpy
    C, D, O, n = bpy.context, bpy.data, bpy.ops, 'tmp-nodedims'
    def obj_tmp():
        (d := D.meshes.new(n)).from_pydata([(-(r := 1),-r,0),(r,-r,0),(r,r,0),(-r,r,0)],[],[(0,1,2,3)])
        o = D.objects.new(n, d)
        (m := D.materials.new(n)).use_nodes = 1
        m.node_tree.nodes.clear()
        d.materials.append(m)
        o.modifiers.new(n, 'NODES').node_group = D.node_groups.new(n, 'GeometryNodeTree')
        C.scene.collection.objects.link(o)
        C.view_layer.objects.active = o
        o.select_set(1)
        return o
    def edt_tmp(w):
        for a in C.screen.areas:
            if not a: continue
            if a.ui_type == w: return a, None
            e, u = a, a.ui_type
        e.ui_type = w
        return e, u
    exclude = set(p + x for x in ('', 'Tree', 'CustomGroup'))
    include = set({'ShaderNode':[], 'GeometryNode':('NodeGroupInput', 'NodeGroupOutput')}[p])
    l = [v for v in dir(bpy.types) if (v.startswith(p) or v in include) and v not in exclude]
    e, u, o, r, s = None, None, None, [], C.preferences.view.ui_scale
    O.object.select_all(action='DESELECT')
    try:
        C.preferences.view.ui_scale = 1
        o = obj_tmp()
        t = {'ShaderNode':o.data.materials[0].node_tree, 'GeometryNode':o.modifiers[0].node_group}[p]
        e, u = edt_tmp(p + 'Tree')
        for v in l: t.nodes.new(v)
        O.wm.redraw_timer(type='DRAW_WIN', iterations=1)
        for v in t.nodes: r.append((v.type, tuple(map(int, v.dimensions))))
    finally:
        C.preferences.view.ui_scale = s
        if e and u: e.ui_type = u
    for c in D.objects, D.meshes, D.materials, D.node_groups: c.remove(c[n])
    O.object.delete(use_global=0)
    return r
